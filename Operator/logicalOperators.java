public class logicalOperators {
    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;

        // Logical Operators
        System.out.println("a && b : " + (a && b));   // AND
        System.out.println("a || b : " + (a || b));   // OR
        System.out.println("!a     : " + (!a));       // NOT
    }
}
